from config import *


def get_response():
    """
    Function to handle a button press

    Returns:
        int
    """
    while True:
        if not BUTTON_UP.value():
            return 1
        elif not BUTTON_DOWN.value():
            return 2
        elif not BUTTON_RIGHT.value():
            return 3
        elif not BUTTON_LEFT.value():
            return 4
        elif not BUTTON_SUBMIT.value():
            return 5
